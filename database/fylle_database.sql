 CASE 
	WHEN LENGTH(STRING_SPLIT)  - LENGTH( REPLACE ( STRING_SPLIT, "|", "") ) = 0 # INGEN ORDSPLITT
	THEN CONCAT (
			"<span class = 'ordSplitt0'>"
			, STRING_SPLIT
			, "</SPAN>"
			)

	WHEN LENGTH(STRING_SPLIT)  - LENGTH( REPLACE ( STRING_SPLIT, "|", "") ) = 1 # BYTTER UT VED 1 ORDSPLITT
	THEN CONCAT (
			"<span class = 'ordSplitt0'>"
			, SUBSTRING(STRING_SPLIT,1,LOCATE('|',STRING_SPLIT )-1) 
			, "</SPAN>"
			, "<span class = 'ordSplitt1'>"
			, SUBSTRING(STRING_SPLIT ,LOCATE('|',STRING_SPLIT)+1 ,LENGTH(STRING_SPLIT) - LOCATE('|',STRING_SPLIT, LOCATE('|',STRING_SPLIT )+1) + 1)
			, "</SPAN>"

			)


	WHEN LENGTH(STRING_SPLIT)  - LENGTH( REPLACE ( STRING_SPLIT, "|", "") ) = 2 # BYTTER UT VED 2 ORDSPLITT
	THEN CONCAT (
			"<span class = 'ordSplitt0'>"
			, SUBSTRING(STRING_SPLIT,1,LOCATE('|',STRING_SPLIT )-1) 
			, "</SPAN>"
			, "<span class = 'ordSplitt1'>"
			, SUBSTRING(STRING_SPLIT,LOCATE('|',STRING_SPLIT)+1,LOCATE('|',STRING_SPLIT, LOCATE('|',STRING_SPLIT )+1)-LOCATE('|',STRING_SPLIT)-1)
			, "</SPAN>"
			, "<span class = 'ordSplitt2'>"
			, SUBSTRING(STRING_SPLIT,LOCATE('|',STRING_SPLIT, LOCATE('|',STRING_SPLIT )+1)+1,LENGTH(STRING_SPLIT)-LOCATE('|',STRING_SPLIT, LOCATE('|',STRING_SPLIT, LOCATE('|',STRING_SPLIT )+1)+1)) 
			, "</SPAN>"
			)
			
	ELSE NULL END 
